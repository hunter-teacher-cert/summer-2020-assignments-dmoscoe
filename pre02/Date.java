public class Date {
	
	public static void main(String[] args) {
		String day = "Thursday";
		int date = 18;
		System.out.println("American format:");
		System.out.println(day + ", June " + date + ", 2020");
		System.out.println("European format:");
		System.out.println(day +" " + date + " " + "June 2020");
	}
}